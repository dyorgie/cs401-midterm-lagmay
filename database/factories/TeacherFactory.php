            'first_name' => fake()->firstName(),
            'last_name'=> fake()->lastName(),
            'email'=> fake()->email(),
            'department' => fake()->randomElement(['Mathematics','Science', 'PE']),
            'birthday' => fake()->date(),
        ];
    }
}
